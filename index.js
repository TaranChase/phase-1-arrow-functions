// using function expression to divide a number by 100.
divide = function(num) {
    return num/100;
}
console.log(divide(2000));

// using arrow function to multiply a number by itself.
let square = (a) => a*a;
console.log(square(5));

// using arrow function to add output the sum of two given numbers.
let add = (a, b) => a+b;
console.log(add(2, 2));